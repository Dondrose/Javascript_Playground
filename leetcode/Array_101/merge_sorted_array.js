var merge = function(nums1 = [1], m = 1, nums2 = [], n = 0) {
  
    starting_position = nums1.length - n;

    if (m < 200 && n < 0) return 1;
    if (m + n < 1 || m + n > 200) return 0;

    for(var i in nums1) {
        if (i < Math.pow(-10,9)) return;
    }

    for(var i in nums2) {
        if (i > Math.pow(10,9)) return;
    }

    for (let i = 0; i < n; i++) {
        nums1.splice(starting_position + i, 1, nums2[i]);
    }

    nums1.sort(function(a, b) {
        return a - b;
    });
    return nums1;

};
